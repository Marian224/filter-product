const grid = document.getElementById("productGrid");
const findBtn = document.getElementById("findBtn");
const countLabel = document.getElementById("count");
const priceRange = document.getElementById("priceFilter");
const priceValueLabel = document.getElementById("priceValue");
const deviceFilter = document.getElementById("deviceFilter");
const modelFilter = document.getElementById("modelFilter");
const statusFilter = document.getElementById("statusFilter");



const formatDevice = (device) =>
    device === "iphone" ? "iPhone" : device === "ipad" ? "iPad" : device;

const formatCondition = (condition) =>
    condition === "new" ? "Новий" : condition === "used" ? "Б/У" : condition;

const populateModelOptions = () => {
    if (!modelFilter || !deviceFilter) {
        return;
    }

    const selectedDevice = deviceFilter.value;
    const models = products
        .filter((p) => selectedDevice === "all" || p.device === selectedDevice)
        .map((p) => p.model);

    const uniqueModels = [...new Set(models)].sort((a, b) => a.localeCompare(b));
    const previousValue = modelFilter.value;

    modelFilter.innerHTML = '<option value="all">Всі моделі</option>';
    uniqueModels.forEach((model) => {
        modelFilter.insertAdjacentHTML(
            "beforeend",
            `<option value="${model}">${model}</option>`,
        );
    });

    modelFilter.value = uniqueModels.includes(previousValue) ? previousValue : "all";
};

priceRange.addEventListener("input", () => {
    priceValueLabel.textContent = priceRange.ariaValue;
});

const updatePriceLabel = () => {
    if (!priceRange || !priceValueLabel) {
        return;
    }
    priceValueLabel.textContent = priceRange.value;
};

if (priceRange && priceValueLabel) {
    priceRange.addEventListener("input", updatePriceLabel);
    priceRange.addEventListener("change", updatePriceLabel);
    updatePriceLabel();
}

const render = (data) => {
    grid.innerHTML = "";
    countLabel.textContent = data.length;

    if (data.length === 0) {
        grid.innerHTML =
            "<h2 style='width: 100%; text-aalign: center'>Нічого не знайдено</h2>"
        return;
    }


    data.forEach((p) => {
        const html = `
    <div class="product-card">
       <img src="${p.img}" alt="${p.name}">
         <div style="font-size: 12px; color: var(--accent)">${p.brand}</div>
          <div style="font-size: 12px; color: var(--accent)">${formatDevice(p.device)}</div>
         <h3>${p.name}</h3>
        <p style="color: var(--text-dim); font-size: 14px">Колір: ${p.color}</p>
          <p style="color: var(--text-dim); font-size: 14px">Модель: ${p.model}</p>
          <p style="color: var(--text-dim); font-size: 14px">Пам'ять: ${p.memory}</p>
          <p style="color: var(--text-dim); font-size: 14px">Стан: ${formatCondition(p.condition)}</p>
        <p class="price">$${p.price}</p>
       <button class="search-btn" style="padding: 8px; font-size: 14px">У кошик</button>
    </div>
    `;
        grid.insertAdjacentHTML("beforeend", html);
    });
};

const applyFilters = () => {
    const nameQuery = document
        .getElementById("nameFilter")
        .value.toLowerCase()
        .trim();
    const brandQuery = document.querySelector(
        'input[name="brand"]:checked',
    ).value;
    const categoryFilter = document.getElementById("categoryFilter");
    const catQuery = categoryFilter ? categoryFilter.value : "all";
    const deviceQuery = deviceFilter ? deviceFilter.value : "all";
    const modelQuery = modelFilter ? modelFilter.value : "all";
    const statusQuery = statusFilter ? statusFilter.value : "all";
    const maxPrice = Number(priceRange.value);
    const sortOrderSelect = document.getElementById("sortOrder");
    const sortOrder = sortOrderSelect ? sortOrderSelect.value : "none";

    // Вибираємо тільки відмічені значення пам'яті
    const selectedMemory = Array.from(
        document.querySelectorAll(".memory-filter:checked"),
    ).map((cb) => cb.value);

    // const catQuery = document.getElementById("categoryFilter").value;
    // const maxPrice = Number(priceRange.value);
    // const sortOrder = document.getElementById("sortOrder").value;
    const selectedColors = Array.from(
        document.querySelectorAll(".color-filter:checked"),
    ).map((cb) => cb.value);

    let filtered = products.filter((p) => {
        const matchName = p.name.toLowerCase().includes(nameQuery);
        const matchBrand = brandQuery === "all" || p.brand === brandQuery;
        const matchCat = catQuery === "all" || p.category === catQuery;
        const matchPrice = p.price <= maxPrice;
        const matchColor = selectedColors.length === 0 || selectedColors.includes(p.color);
        const matchDevice = deviceQuery === "all" || p.device === deviceQuery;
        const matchModel = modelQuery === "all" || p.model === modelQuery;
        const matchMemory =
            selectedMemory.length === 0 || selectedMemory.includes(p.memory);
        const matchCondition = statusQuery === "all" || p.condition === statusQuery;

        return (
            matchDevice &&
            matchModel &&
            matchMemory &&
            matchCondition &&
            matchPrice &&
            matchName &&
            matchBrand &&
            matchCat &&
            matchColor
        );
    });

    if (sortOrder === "lowToHigh") {
        filtered.sort((a, b) => a.price - b.price);
    } else if (sortOrder === "highToLow") {
        filtered.sort((a, b) => b.price - a.price);
    }
    render(filtered)
}
